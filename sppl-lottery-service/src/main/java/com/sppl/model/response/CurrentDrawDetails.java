package com.sppl.model.response;

public class CurrentDrawDetails {

	String id;
	String drawDate;
	String drawClosure;
	String status;
	String drawNo;
	String jackpot;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getDrawDate() {
		return drawDate;
	}

	public void setDrawDate(String drawDate) {
		this.drawDate = drawDate;
	}

	public String getDrawClosure() {
		return drawClosure;
	}

	public void setDrawClosure(String drawClosure) {
		this.drawClosure = drawClosure;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getDrawNo() {
		return drawNo;
	}

	public void setDrawNo(String drawNo) {
		this.drawNo = drawNo;
	}

	public String getJackpot() {
		return jackpot;
	}

	public void setJackpot(String jackpot) {
		this.jackpot = jackpot;
	}

}
